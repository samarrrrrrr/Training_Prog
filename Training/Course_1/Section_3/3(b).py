"""
2. Given two sets:
    set1 = {6, 8, 3, 5}, set2 = {1, 6, 7, 3, 4}
    Perform the following operations:
    1. Union of both sets
    2. Intersection of both sets
"""
set1 = {6, 8, 3, 5}
set2 = {1, 6, 7, 3, 4}
print(set1)
print(set2)
print("The Union of both : ")
print (set1 | set2)
print("The Intersection of both : ")
print (set1 & set2)